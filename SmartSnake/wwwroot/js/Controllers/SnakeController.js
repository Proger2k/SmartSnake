function movement() 
{
    document.addEventListener("keydown", (e) => {
        switch (e.keyCode) {
            case 39:
                // right
                
                break
            case 37:
                // left
                break
        }
    })

    document.addEventListener("keyup", (e) => {
        switch (e.keyCode) {
            case 39:
                break

            case 37:
                break
        }
    })
}